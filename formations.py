def get_formation(name, mirrored=False):
    coords = FORMATIONS.get(name, FORMATIONS["4-4-2"])
    if mirrored:
        # For Team B, we mirror X AND invert Y role logic slightly if needed, 
        # but usually soccer mirroring is just flipped across the halfway line.
        return [(1.0 - x, y) for x, y in coords]
    return coords
